#include "../../../includes/cub3d.h"

int	check_empty(char *line, char c)
{
	int	i;

	i = 0;
	while (line[i])
	{
		if (c == 'N' && line[i] != ' ' && line[i] != '\n')
			return (1);
		if (c != 'N' && line[i] != ' ')
			return (1);
		i++;
	}
	return (0);
}
